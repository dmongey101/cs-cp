
public class Ex2 {

	public static void main(String[] args) {
		
		double tempInCelsius = 36.6;
		
		double tempInFahrenheit = (tempInCelsius * 9.0/5) + 32.0;
		
		System.out.println(tempInCelsius + "°C is " + tempInFahrenheit + "°F");
		
	}

}

// 0C = 32F
// 12C = 53.6F 
// 36.6C = 97.88000000000001F
// 50C = 122F
// 100C = 212F
